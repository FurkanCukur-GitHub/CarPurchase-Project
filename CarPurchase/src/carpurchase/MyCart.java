    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        boolean a = false;
        
        for(int i = 0; i < currentCartNumber; i++)
        {
            if(CartTable.isRowSelected(i))
                a = true;
        }
        if(!a)
        {
            JOptionPane.showMessageDialog(this, "Select A Car to Delete");
        }
        else
        {
            int i = CartTable.getSelectedRow();
            clearCarInformations();
            if (i >= 0)
            {
                try {
                    cart.deleteCart(i);
                } catch (IOException ex) {
                    java.util.logging.Logger.getLogger(MyCart.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                }    
                getShowCartInformations();
            }
            JOptionPane.showMessageDialog(this, "Car Deleted Successfully");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

