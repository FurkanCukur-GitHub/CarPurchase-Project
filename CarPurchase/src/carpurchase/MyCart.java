    private void clearCarInformations()
    {
        DefaultTableModel model = (DefaultTableModel)CartTable.getModel(); 
        currentCartNumber = cart.getCurrentCartNumber();
        for (int i = (currentCartNumber - 1); i >= 0; i--)
        {
            model.removeRow(i);
        }
    }
    
    private void getShowCartInformations()
    {
        currentCartNumber = cart.getCurrentCartNumber();
        cartInformations = cart.getCartInformations();
                
        for (int i = 0; i < currentCartNumber; i++)
        {
            for (int j = 0; j < 5; j++)
            {
                row[j] = cartInformations[i][j];
            }
            
            DefaultTableModel model = (DefaultTableModel)CartTable.getModel();
            model.addRow(row);
        }
    }

